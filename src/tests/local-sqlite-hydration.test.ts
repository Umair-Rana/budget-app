class RecordingHydrationDriver implements LocalSqliteDriver {
  committed = false
  failOnSqlIncludes?: string
  rolledBack = false
  runs: Array<{
    inTransaction: boolean
    params?: LocalSqliteStatementParams
    sql: string
  }> = []
  transactionCalls = 0
  private inTransaction = false

  async exec() {
    return undefined
  }

  async query<T>() {
    return [] as T[]
  }

  async run(sql: string, params?: LocalSqliteStatementParams) {
    this.runs.push({
      inTransaction: this.inTransaction,
      params,
      sql,
    })

    if (this.failOnSqlIncludes && sql.includes(this.failOnSqlIncludes)) {
      throw new Error(`planned failure for ${this.failOnSqlIncludes}`)
    }
  }

  async transaction<T>(work: () => Promise<T>) {
    this.transactionCalls += 1
    this.inTransaction = true

    try {
      const result = await work()
      this.committed = true
      return result
    } catch (error) {
      this.rolledBack = true
      throw error
    } finally {
      this.inTransaction = false
    }
  }
}

