describe('local SQLite hydration upsert helpers', () => {
  it('generates SQLite upsert SQL with stable params', () => {
    const statement = createLocalUpsertStatement({
      row: {
        household_id: 'household-1',
        id: 'account-1',
        name: 'Cash',
      },
      tableName: 'accounts',
    })

    expect(statement).toEqual({
      params: ['household-1', 'account-1', 'Cash'],
      sql: 'insert into accounts (household_id, id, name) values (?, ?, ?) on conflict(id) do update set household_id = excluded.household_id, name = excluded.name',
    })
  })

  it('generates metadata upsert SQL for last pulled timestamps', () => {
    const statement = createLocalSyncMetadataUpsertStatement({
      entityType: 'accounts',
      householdId: 'household-1',
      pulledAt: now.toISOString(),
    })

    expect(statement.sql).toContain('insert into local_sync_metadata')
    expect(statement.sql).toContain('last_pulled_at = excluded.last_pulled_at')
    expect(statement.params).toEqual([
      'household-1:accounts',
      'household-1',
      'accounts',
      now.toISOString(),
      null,
      null,
      now.toISOString(),
      now.toISOString(),
    ])
  })
})

describe('Supabase to local SQLite hydration', () => {
  it('reads all Supabase finance repositories with include-all options', async () => {
    const { driver, input } = createHydrationInput()

    await hydrateSupabaseToLocalSqlite(input)

    const includeAllRows = {
      includeArchived: true,
      includeDeleted: true,
    }

    expect(input.dataSource.accounts.getAll).toHaveBeenCalledWith(includeAllRows)
    expect(input.dataSource.bills.getAll).toHaveBeenCalledWith(includeAllRows)
    expect(input.dataSource.budgets.getAll).toHaveBeenCalledWith(includeAllRows)
    expect(input.dataSource.categories.getAll).toHaveBeenCalledWith(
      includeAllRows,
    )
    expect(input.dataSource.goals.getAll).toHaveBeenCalledWith(includeAllRows)
    expect(input.dataSource.loans.getAll).toHaveBeenCalledWith(includeAllRows)
    expect(input.dataSource.recurringBills.getAll).toHaveBeenCalledWith(
      includeAllRows,
    )
    expect(input.dataSource.recurringTransactions.getAll).toHaveBeenCalledWith(
      includeAllRows,
    )
    expect(input.dataSource.transactions.getAll).toHaveBeenCalledWith(
      includeAllRows,
    )
    expect(driver.transactionCalls).toBe(1)
  })

  it('runs local projection writes and metadata updates inside one transaction', async () => {
    const { driver, input } = createHydrationInput()

    const result = await hydrateSupabaseToLocalSqlite(input)

    expect(result.errors).toEqual([])
    expect(result.tables).toMatchObject({
      accounts: 1,
      bills: 1,
      budgets: 1,
      categories: 1,
      goals: 1,
      householdMembers: 1,
      households: 1,
      loans: 1,
      notifications: 4,
      recurringBills: 1,
      recurringTransactions: 1,
      transactions: 1,
    })
    expect(driver.committed).toBe(true)
    expect(driver.runs.length).toBeGreaterThan(0)
    expect(driver.runs.every((run) => run.inTransaction)).toBe(true)

    const metadataRuns = driver.runs.filter((run) =>
      run.sql.includes('insert into local_sync_metadata'),
    )

    expect(metadataRuns).toHaveLength(localHydrationEntityTypes.length)
    expect(metadataRuns.map((run) => run.params?.[2])).toEqual(
      localHydrationEntityTypes,
    )
  })

  it('soft-deletes existing local rows before upserting cloud rows', async () => {
    const { driver, input } = createHydrationInput()

    await hydrateSupabaseToLocalSqlite(input)

    expect(driver.runs[0]).toEqual({
      inTransaction: true,
      params: [now.toISOString(), now.toISOString(), 'household-1'],
      sql: 'update household_members set deleted_at = ?, updated_at = ? where household_id = ? and deleted_at is null',
    })
  })

  it('preserves deleted and archived values from cloud domain rows', async () => {
    const { driver, input } = createHydrationInput()

    await hydrateSupabaseToLocalSqlite(input)

    const accountUpsert = driver.runs.find((run) =>
      run.sql.startsWith('insert into accounts'),
    )

    expect(accountUpsert?.params).toContain('2026-06-22T00:00:00.000Z')
    expect(accountUpsert?.params).toContain('2026-06-23T00:00:00.000Z')
  })

  it('returns a clear error result and lets the driver rollback on partial failure', async () => {
    const driver = new RecordingHydrationDriver()
    driver.failOnSqlIncludes = 'insert into transactions'
    const { input } = createHydrationInput(driver)

    const result = await hydrateSupabaseToLocalSqlite(input)

    expect(result.errors).toEqual(['planned failure for insert into transactions'])
    expect(driver.rolledBack).toBe(true)
    expect(driver.committed).toBe(false)
    expect(result.tables).toEqual({
      accounts: 0,
      bills: 0,
      budgets: 0,
      categories: 0,
      goals: 0,
      householdMembers: 0,
      households: 0,
      loans: 0,
      notifications: 0,
      recurringBills: 0,
      recurringTransactions: 0,
      transactions: 0,
    })
  })
})
